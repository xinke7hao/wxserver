package com.hunantv.mbp.dto;

/**
 * @author XuYanbo
 *
 */
public class BossSystemTag {

	private String status;
	private String default_flag_str;
	private String kind;
	private String name;
	private String created_at;
	private String updated_at;
	private String default_flag;
	private String kind_str;
	private String id;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDefault_flag_str() {
		return default_flag_str;
	}
	public void setDefault_flag_str(String default_flag_str) {
		this.default_flag_str = default_flag_str;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCreated_at() {
		return created_at;
	}
	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}
	public String getUpdated_at() {
		return updated_at;
	}
	public void setUpdated_at(String updated_at) {
		this.updated_at = updated_at;
	}
	public String getDefault_flag() {
		return default_flag;
	}
	public void setDefault_flag(String default_flag) {
		this.default_flag = default_flag;
	}
	public String getKind_str() {
		return kind_str;
	}
	public void setKind_str(String kind_str) {
		this.kind_str = kind_str;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

}
